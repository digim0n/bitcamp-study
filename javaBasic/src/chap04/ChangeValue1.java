package chap04;

public class ChangeValue1 {
    public static void main(String[] args) {
        //년도
        int year = 2002;
        System.out.println("한국 월드컵은 " + year + "년도에 개최되었습니다.");

        // 변수에 다른 값 할당
        year = 2018;
        System.out.println("동계올림픽이 " + year + "년도에 개최되었습니다.");
    }
}
