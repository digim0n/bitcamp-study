package chap06;

public class IfStatement3 {

    public static void main(String[] args) {
        // 비교값을 관리하는 변수 선언 및 초기화
        int a = 2;
        int b = 5;

        System.out.println(" ===== if문 시작 ==== ");
        if (a != b) {
            System.out.println("변수 a와 b는 다르다");
        }
        System.out.println(" ===== if문 종료 ==== ");
    }
}